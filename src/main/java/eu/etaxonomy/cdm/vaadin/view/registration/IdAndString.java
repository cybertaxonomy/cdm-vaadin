package eu.etaxonomy.cdm.vaadin.view.registration;

public class IdAndString {
    int id;
    String label;

    public IdAndString(int id, String label) {
        this.id = id;
        this.label = label;
    }

    public int getId() {
        return id;
    }

    public String getLabel() {
        return label;
    }
}